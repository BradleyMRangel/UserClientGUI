    private void btn_ReguserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ReguserActionPerformed

        
        firstName = txt_Firstname.getText();
        lastName = txt_Lastname.getText();
        newPass = txt_Regpass.getText();
        newUser = txt_Reguser.getText();
        

        if (errorCheck(firstName, lastName, newUser, newPass)){
            regU = "REGU " + "\"" + newUser + "\"" + " " + "\"" + newPass + "\"" + " " + "\"" + firstName + "\"" + " " + "\""+lastName+"\""+"\r";
            System.out.println(regU);
            
            try{
                //clientLog.TCPClient(regU);
                TCPConnection conn = TCPConnection.getInstance();
                
                String serverResponse = conn.readWrite(regU);
                if (serverResponse.equalsIgnoreCase("REGR F")){
                    errorUser.setText("The username already exists.  Please choose another one");
                }
                else if(serverResponse.equalsIgnoreCase("REGR S")){
                    JOptionPane.showMessageDialog(successPopup, "You've successfully registered! Bienvenidos","SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    
                    //Logs
                    LOGU = "LOGU " + "\"" + newUser + "\"" + " " + "\""+ newPass + "\"" + "\r";
                    serverResponse = conn.readWrite(LOGU);
                    
                    
                    //where we will redirect to screen after login
                    EnrolledClasses schedule = new EnrolledClasses();
                    this.setVisible(false);
                    schedule.setVisible(true);
                }
                
                else 
                    JOptionPane.showMessageDialog(popupWindow, "No response from the server","FYI", JOptionPane.INFORMATION_MESSAGE);
                
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(popupWindow, "There was an error in the connection","CRITICAL ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }

    
        
    }//GEN-LAST:event_btn_ReguserActionPerformed

