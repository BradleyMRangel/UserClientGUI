    private boolean errorCheck(String first,String last,String user,String pass){
        boolean Error = true;
        
        while(Error){
            if(!first.matches("[a-zA-Z]+")){
                errorFirst.setText("This field cannot be blank and must consist of letters (a-z or A-Z) or numbers (0-9)");
                Error = false;
            }
            else
                errorFirst.setText("");
            if(!last.matches("[a-zA-Z]+")){
                errorLast.setText("This field cannot be blank and must consist of letters (a-z or A-Z) or numbers (0-9)");
                Error = false;
            }
            else
                errorLast.setText("");
            if(!user.matches("[a-zA-Z0-9]+")){
                errorUser.setText("This field cannot be blank and must consist of letters (a-z or A-Z) or numbers (0-9)");
                Error = false;
            }
            else
                errorUser.setText("");            
//            if(!pass.matches("[a-zA-Z0-9]+")){
//                errorPass.setText("This field cannot be blank and must consist of letters (a-z or A-Z) or numbers (0-9)");
//                Error = false;
//            }
//            else{
//                errorPass.setText("");
//            }
            if(txt_Regpass.getText().equals("")){
                errorPass.setText("This field is required");
            }
            else if(txt_Regpass.getText().contains(" ")){
                errorPass.setText("Password cannot contain spaces");
                txt_Regpass.setText("");
            }
            else
                errorPass.setText("");
            
            break;
        }
        return Error;
        
    }
